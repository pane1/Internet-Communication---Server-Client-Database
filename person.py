class Person:

    def __init__(self, StudName, id, passw, l1, l2, l3, l4, mt, e1, e2, e3, e4):
        """Create a new person object by calling it with the new person's
        first and last name.

        Example: new_person = Person(first_name="John",
        last_name="Smith").

        """
        self.StudName = StudName
        self.id = id
        self.passw = passw
        self.l1 = l1
        self.l2 = l2
        self.l3 = l3
        self.l4 = l4
        self.mt = mt
        self.e1 = e1
        self.e2 = e2
        self.e3 = e3
        self.e4 = e4 

